    private void populateTableFromDatabase() {
        
      DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
      model.setRowCount(0); // Clear existing data

    try {
        Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/adminapplication", "root", "");
        Statement statement = connection.createStatement();
        ResultSet resultSet = statement.executeQuery("SELECT Firstname, Middlename, Lastname, Age, Sex, Destination, SeatClass FROM adminapplication");

        while (resultSet.next()) {
            
            // To Show the specific data to Registration List Table
            String firstname = resultSet.getString("Firstname"); 
            String middlename = resultSet.getString("Middlename");
            String lastname = resultSet.getString("Lastname");
            int age = resultSet.getInt("Age");
            String sex = resultSet.getString("Sex");
            String destination = resultSet.getString("Destination");
            String seatClass = resultSet.getString("SeatClass");

            Object[] rowData = {firstname, middlename, lastname, age, sex, destination, seatClass}; 
            model.addRow(rowData);
        }

            resultSet.close();
            statement.close();
            connection.close();
            
        } catch (Exception e) {
        e.printStackTrace();
      }
    }
    
